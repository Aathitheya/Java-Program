public class Nestedifelse {
    public static void main(String[] args){
        int n=7;
        if(n==0)
        System.out.println("Neither negative nor positive");
        else
        if(n%2==0)
        System.out.println(n +" is Even number");
        else
        System.out.println(n +"is Odd number");
    }
}